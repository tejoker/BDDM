_CITE_CMD_RE = re.compile(r"\\cite[a-zA-Z*]*\{([^}]*)\}")
_BIBITEM_RE = re.compile(r"\\bibitem(?:\[[^\]]*\])?\{([^}]*)\}")


def _extract_cited_refs_from_tex(tex_paths: list[Path]) -> list[str]:
    """Extract citation keys / bibitem labels from TeX sources for grounding hints."""
    refs: set[str] = set()
    for p in tex_paths:
        try:
            text = p.read_text(encoding="utf-8", errors="replace")
        except Exception:
            continue

        for m in _CITE_CMD_RE.finditer(text):
            payload = m.group(1)
            for token in payload.split(","):
                key = token.strip()
                if key:
                    refs.add(key)

        for m in _BIBITEM_RE.finditer(text):
            key = m.group(1).strip()
            if key:
                refs.add(key)

    return sorted(refs)


def _records_to_dicts(records: list) -> list[dict]:
    out: list[dict] = []
    for r in records:
        if isinstance(r, dict):
            out.append(
                {
                    "step": int(r.get("step", 0)),
                    "attempt": int(r.get("attempt", 0)),
                    "tactic": str(r.get("tactic", "")),
                    "model_turns": int(r.get("model_turns", 0)),
                    "result": str(r.get("result", "")),
                    "detail": str(r.get("detail", "")),
                }
            )
            continue
        out.append(
            {
                "step": r.step,
                "attempt": r.attempt,
                "tactic": r.tactic,
                "model_turns": r.model_turns,
                "result": r.result,
                "detail": r.detail,
            }
        )
    return out



#!/usr/bin/env python3
"""Retroactively filter Desol/PaperTheory/Paper_*.lean export lines.

Some legacy stubs were generated by an older pipeline that emitted bare
identifiers in the `export Paper_<id> (...)` line that never appear as
top-level declarations in the file. Lean rejects this with `Unknown constant
Paper_<id>.<name>`, knocking the whole paper out of `lake build` and out of
the `Desol/PaperImportsAnchor.lean` REPL fallback.

This script reads each Paper_*.lean, parses its top-level declarations
(`def`/`abbrev`/`opaque`/`noncomputable def`/`axiom`/`theorem`/`lemma`),
and rewrites the `export` line to keep only names that actually appear.
Idempotent (no rewrite when the file is already clean).

Usage:
    python3 scripts/repair_paper_theory_exports.py [--paper-theory-dir DIR] [--dry-run]
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

PAPER_THEORY_DIR = Path("Desol/PaperTheory")

# Match a top-level decl head and capture its name. We do NOT use `declaration_name`
# from paper_symbol_inventory directly because we want a string-level pass that
# operates on the rendered file, not on parsed plan dicts.
_DECL_RE = re.compile(
    r"^\s*(?:noncomputable\s+|private\s+)*(?:def|abbrev|opaque|axiom|theorem|lemma)\s+([A-Za-z_Ͱ-Ͽἀ-῿][A-Za-z0-9_Ͱ-Ͽἀ-῿']*)",
    re.MULTILINE,
)
_EXPORT_LINE_RE = re.compile(
    r"^(export\s+[A-Za-z_][A-Za-z0-9_]*\s*\()([^)]*)(\))",
    re.MULTILINE,
)


def _defined_names(text: str) -> set[str]:
    return {m.group(1) for m in _DECL_RE.finditer(text)}


def repair_file(path: Path, *, dry_run: bool = False) -> dict:
    text = path.read_text(encoding="utf-8")
    defined = _defined_names(text)
    summary = {"path": str(path), "removed": 0, "kept": 0, "changed": False}

    def _filter(match: re.Match[str]) -> str:
        head, body, tail = match.group(1), match.group(2), match.group(3)
        names = body.split()
        kept = [n for n in names if n in defined]
        removed = len(names) - len(kept)
        summary["removed"] += removed
        summary["kept"] += len(kept)
        return head + " ".join(kept) + tail

    new_text = _EXPORT_LINE_RE.sub(_filter, text)
    if new_text == text:
        return summary
    summary["changed"] = True
    if not dry_run:
        path.write_text(new_text, encoding="utf-8")
    return summary


def main() -> int:
    parser = argparse.ArgumentParser(description="Filter Desol/PaperTheory/Paper_*.lean export lines")
    parser.add_argument("--paper-theory-dir", type=Path, default=PAPER_THEORY_DIR)
    parser.add_argument("--dry-run", action="store_true")
    args = parser.parse_args()

    base = args.paper_theory_dir
    if not base.exists():
        print(f"No such directory: {base}", file=sys.stderr)
        return 2

    summaries = [repair_file(p, dry_run=args.dry_run) for p in sorted(base.rglob("Paper_*.lean"))]
    total_removed = sum(s["removed"] for s in summaries)
    changed = sum(1 for s in summaries if s["changed"])
    for s in summaries:
        if s["removed"]:
            print(f"{s['path']}: -{s['removed']} undefined export name(s){' (dry-run)' if args.dry_run else ''}")
    print()
    print(f"Summary: {changed} file(s) {'would change' if args.dry_run else 'changed'}, -{total_removed} undefined export name(s).")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

def test_recover_paper_dry_run_reports_placeholders(tmp_path: Path) -> None:
    """Without --write, the tool only previews what it would attempt; no
    Mistral calls happen and no files mutate."""
    paper = "1234.56789"
    out_dir = tmp_path / "output"
    out_dir.mkdir()
    lean_path = out_dir / f"{paper}.lean"
    lean_path.write_text(_placeholder_lean(), encoding="utf-8")
    # Provide a source LaTeX so `would_attempt` is True.
    repo = tmp_path / "reproducibility" / "paper_agnostic_golden10_results" / paper
    repo.mkdir(parents=True)
    (repo / "extracted_theorems.json").write_text(json.dumps({
        "entries": [{"label": "thm:foo", "statement": "If x>0 then x+1>0."}],
    }), encoding="utf-8")

    summary = recover_paper(paper, project_root=tmp_path, write=False)
    assert summary["dry_run"] is True
    assert summary["placeholders_detected"] == 1
    assert summary["results"][0]["label"] == "thm:foo"
    assert summary["results"][0]["would_attempt"] is True
    # File was not modified.
    assert lean_path.read_text() == _placeholder_lean()

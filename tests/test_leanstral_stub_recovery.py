"""Tests for leanstral_stub_recovery (placeholder detection + LaTeX lookup).

Tests that exercise the actual translator are gated behind `slow` markers so
the default suite stays hermetic."""

from __future__ import annotations

import json
from pathlib import Path

import pytest

from leanstral_stub_recovery import (
    PlaceholderBlock,
    _build_recovery_hint,
    _replace_placeholder_in_text,
    detect_placeholders,
    find_source_latex,
    recover_paper,
)


def _placeholder_lean(label: str = "thm:foo", name: str = "foo", reason: str = "claim_shape_mismatch:eq->ineq") -> str:
    return (
        "-- Auto-generated by arxiv_to_lean.py\n"
        "namespace ArxivPaper\n"
        "\n"
        f"-- [theorem] {label}\n"
        "-- Statement (LaTeX): Some truncated LaTeX...\n"
        f"-- Translation: BLOCKED — {reason}\n"
        "-- Attempted signature rejected by translation_acceptance_gate.\n"
        f"theorem {name} : False := by sorry\n"
        "\n"
        "end ArxivPaper\n"
    )


def test_detect_placeholders_finds_false_sorry() -> None:
    blocks = detect_placeholders(_placeholder_lean())
    assert len(blocks) == 1
    assert blocks[0].label == "thm:foo"
    assert blocks[0].name == "foo"
    assert "claim_shape_mismatch" in blocks[0].failure_reason


def test_detect_placeholders_finds_true_trivial() -> None:
    """`theorem foo : True := trivial` is also a placeholder pattern."""
    src = (
        "namespace ArxivPaper\n"
        "-- [theorem] thm:bar\n"
        "-- Statement (LaTeX): bar\n"
        "theorem bar : True := trivial\n"
        "end ArxivPaper\n"
    )
    blocks = detect_placeholders(src)
    assert len(blocks) == 1
    assert blocks[0].name == "bar"


def test_detect_placeholders_skips_real_theorems() -> None:
    src = (
        "namespace ArxivPaper\n"
        "-- [theorem] thm:real\n"
        "-- Statement (LaTeX): something\n"
        "theorem real_thm (n : Nat) : n + 0 = n := by simp\n"
        "end ArxivPaper\n"
    )
    blocks = detect_placeholders(src)
    assert blocks == []


def test_detect_placeholders_handles_multiple_blocks() -> None:
    src = (
        "namespace ArxivPaper\n"
        "-- [theorem] thm:a\n"
        "-- Statement (LaTeX): a\n"
        "theorem a : False := by sorry\n"
        "\n"
        "-- [theorem] thm:b\n"
        "-- Statement (LaTeX): b\n"
        "-- Translation: BLOCKED — lean_elaboration_failed\n"
        "theorem b : False := by sorry\n"
        "\n"
        "-- [theorem] thm:c\n"
        "theorem c (x : Nat) : x = x := rfl\n"
        "end ArxivPaper\n"
    )
    blocks = detect_placeholders(src)
    assert [b.name for b in blocks] == ["a", "b"]



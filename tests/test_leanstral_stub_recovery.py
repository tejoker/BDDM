def test_find_source_latex_returns_empty_when_missing(tmp_path: Path) -> None:
    latex, meta = find_source_latex("9999.99999", "thm:nonexistent", tmp_path)
    assert latex == ""
    assert meta == {}


def test_find_source_latex_reads_extracted_theorems(tmp_path: Path) -> None:
    """The lookup walks the canonical `extracted_theorems.json` paths and
    matches by label."""
    paper = "1234.56789"
    repo = tmp_path / "reproducibility" / "paper_agnostic_golden10_results" / paper
    repo.mkdir(parents=True)
    (repo / "extracted_theorems.json").write_text(json.dumps({
        "paper_id": paper,
        "entries": [
            {"label": "thm:foo", "statement": "If $x>0$ then $x+1>0$.", "source_file": "out.tex"},
            {"label": "thm:bar", "statement": "bar body", "source_file": "out.tex"},
        ],
    }), encoding="utf-8")
    latex, meta = find_source_latex(paper, "thm:foo", tmp_path)
    assert "x>0" in latex
    assert meta["source_file"] == "out.tex"


def test_replace_placeholder_in_text_swaps_signature() -> None:
    lean = _placeholder_lean(name="foo")
    block = detect_placeholders(lean)[0]
    new_sig = "theorem foo (n : Nat) : n + 0 = n"
    out = _replace_placeholder_in_text(lean, block, new_sig)
    assert "theorem foo (n : Nat) : n + 0 = n := by sorry" in out
    assert "theorem foo : False" not in out


def test_replace_placeholder_preserves_block_comments() -> None:
    """Comments around the placeholder (theorem header, LaTeX, BLOCKED) must
    survive the replacement — they're useful provenance for downstream tools."""
    lean = _placeholder_lean(label="thm:keep", name="keep")
    block = detect_placeholders(lean)[0]
    out = _replace_placeholder_in_text(lean, block, "theorem keep (x : Nat) : x = x")
    assert "-- [theorem] thm:keep" in out
    assert "-- Statement (LaTeX): Some truncated LaTeX..." in out
    assert "-- Translation: BLOCKED" in out



# Academic/research-focused User-Agent strings
ACADEMIC_USER_AGENTS = [
    'MathResearchBot/1.0 (Educational Research; +http://example.com/bot)',
    'AcademicScraper/2.0 (University Research Project; contact@university.edu)',
    'MathDataCollector/1.5 (Mathematical Content Analysis; research@institution.edu)',
]


def get_random_user_agent(include_academic: bool = False) -> str:
    """
    Get a random User-Agent string

    Args:
        include_academic: If True, include academic/research User-Agents in the pool

    Returns:
        Random User-Agent string
    """
    agents = USER_AGENTS.copy()
    if include_academic:
        agents.extend(ACADEMIC_USER_AGENTS)

    return random.choice(agents)


def get_rotating_headers(include_academic: bool = False) -> dict:
    """
    Get HTTP headers with a random User-Agent

    Args:
        include_academic: If True, include academic/research User-Agents

    Returns:
        Dictionary with User-Agent and other common headers
    """
    return {
        'User-Agent': get_random_user_agent(include_academic),
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
        'Accept-Language': 'en-US,en;q=0.5',
        'Accept-Encoding': 'gzip, deflate',
        'DNT': '1',
        'Connection': 'keep-alive',
        'Upgrade-Insecure-Requests': '1'
    }
